// English snapshot taken from the DOM on load
const EN = {};

// ================= State (no localStorage) =================
let currentLang = "en";
let currentTheme = "dark";

const htmlEl = document.documentElement;
const langToggle = document.getElementById("langToggle");
const themeToggle = document.getElementById("themeToggle");
const menuToggle = document.getElementById("menuToggle");
const navLinks = document.getElementById("navLinks");
const iconSun = document.getElementById("iconSun");
const iconMoon = document.getElementById("iconMoon");

// Snapshot English strings
document.querySelectorAll("[data-i18n]").forEach((el) => {
  EN[el.dataset.i18n] = el.innerHTML;
});

// ================= Language toggle =================
function applyLang(lang) {
  currentLang = lang;
  const dict = lang === "ar" ? AR : EN;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
  htmlEl.lang = lang;
  htmlEl.dir = lang === "ar" ? "rtl" : "ltr";
  langToggle.textContent = lang === "ar" ? "EN" : "عربي";
  langToggle.setAttribute("aria-label", lang === "ar" ? "Switch to English" : "التبديل إلى العربية");
  document.title = lang === "ar"
    ? "رياض بيطموني — مهندس ذكاء اصطناعي · مطوّر Flutter · تطوير أعمال تقني | دبي"
    : "Riad Baytamouni — AI Engineer · Flutter Developer · Technical BD | Dubai";
}

langToggle.addEventListener("click", () => {
  applyLang(currentLang === "en" ? "ar" : "en");
});

// ================= Theme toggle (dark default, JS variable only) =================
function applyTheme(theme) {
  currentTheme = theme;
  htmlEl.dataset.theme = theme;
  iconSun.hidden = theme === "light";
  iconMoon.hidden = theme === "dark";
}
applyTheme("dark");

themeToggle.addEventListener("click", () => {
  applyTheme(currentTheme === "dark" ? "light" : "dark");
});

// ================= Mobile menu =================
menuToggle.addEventListener("click", () => {
  const open = navLinks.classList.toggle("open");
  menuToggle.setAttribute("aria-expanded", String(open));
});
navLinks.addEventListener("click", (e) => {
  if (e.target.tagName === "A") {
    navLinks.classList.remove("open");
    menuToggle.setAttribute("aria-expanded", "false");
  }
});

// ================= Scroll reveal =================
const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.12, rootMargin: "0px 0px -40px 0px" }
);
document.querySelectorAll(".reveal").forEach((el) => observer.observe(el));

